// Comparision Operator

console.log(2 > 1);
console.log(2 >= 1);
console.log(2 < 1);
console.log(2 == 1);
console.log(2 != 1);

// Comparision between two data type

console.log("2" > 0);
console.log("02" > 0);

/* Avoid This Type Of Code

console.log(null > 0);
console.log(null == 0);
console.log(null >= 0);

console.log(undefined == 0);
console.log(undefined > 0);
console.log(undefined < 0);

*/

// Strictly Comparision

console.log("2" === 2);